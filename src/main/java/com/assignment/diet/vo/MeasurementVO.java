package com.assignment.diet.vo;

public class MeasurementVO {

	private Long referralId;
	private Float weight, height, chest, waist, shoulders, biceps, forearms, legs, thighs;

	public Long getReferralId() {
		return referralId;
	}

	public void setReferralId(Long referralId) {
		this.referralId = referralId;
	}

	public float getWeight() {
		return weight;
	}

	public void setWeight(float weight) {
		this.weight = weight;
	}

	public float getHeight() {
		return height;
	}

	public void setHeight(float height) {
		this.height = height;
	}

	public float getChest() {
		return chest;
	}

	public void setChest(float chest) {
		this.chest = chest;
	}

	public float getWaist() {
		return waist;
	}

	public void setWaist(float waist) {
		this.waist = waist;
	}

	public float getShoulders() {
		return shoulders;
	}

	public void setShoulders(float shoulders) {
		this.shoulders = shoulders;
	}

	public float getBiceps() {
		return biceps;
	}

	public void setBiceps(float biceps) {
		this.biceps = biceps;
	}

	public float getForearms() {
		return forearms;
	}

	public void setForearms(float forearms) {
		this.forearms = forearms;
	}

	public float getLegs() {
		return legs;
	}

	public void setLeg(float legs) {
		this.legs = legs;
	}

	public float getThighs() {
		return thighs;
	}

	public void setThighs(float thighs) {
		this.thighs = thighs;
	}
}
